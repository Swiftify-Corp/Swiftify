import UIKit

extension UIColor {
    class func aqua() -> UIColor? {
        return UIColor(red: 46.0 / 255.0, green: 184.0 / 255.0, blue: 184.0 / 255.0, alpha: 1.0)
    }

    class func violet() -> UIColor? {
        return UIColor(red: 134.0 / 255.0, green: 45.0 / 255.0, blue: 134.0 / 255.0, alpha: 1.0)
    }

    class func shadow() -> UIColor? {
        return UIColor(red: 0.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 0.5)
    }
}